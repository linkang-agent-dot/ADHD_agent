def gws_write(sheet_id, rng, values):
    body = {'data':[{'range': rng, 'values': values}]}
    body_str = json.dumps(body, ensure_ascii=False, separators=(',',':'))
    params_str = json.dumps({'spreadsheetId': sheet_id, 'valueInputOption': 'RAW'}, ensure_ascii=False)
    cmd = [GWS, 'sheets', 'spreadsheets', 'values', 'batchUpdate',
           '--params', params_str, '--json', body_str]
    r = subprocess.run(cmd, capture_output=True, text=True, encoding='utf-8', errors='replace')
    if r.returncode != 0:
        print(f'  FAIL rc={r.returncode} err={r.stderr[:300]}')
        return False
    return True

