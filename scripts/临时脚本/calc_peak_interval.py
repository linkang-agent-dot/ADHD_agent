known_ltv = {0:0, 1:0.207, 2:0.267, 3:0.320, 5:0.418, 7:0.501, 10:0.599, 14:0.711, 17:0.786, 20:0.861, 21:0.90, 28:1.05, 30:1.10}

def interp(d):
    keys = sorted(known_ltv.keys())
    if d in known_ltv: return known_ltv[d]
    for i in range(len(keys)-1):
        if keys[i] <= d <= keys[i+1]:
            t = (d - keys[i]) / (keys[i+1] - keys[i])
            return known_ltv[keys[i]] * (1-t) + known_ltv[keys[i+1]] * t
    return known_ltv[keys[-1]]

ltv_curve = [interp(d) for d in range(31)]
daily_delta = [ltv_curve[0]] + [ltv_curve[d] - ltv_curve[d-1] for d in range(1, 31)]

# 每天每个R级的绝对贡献
daily_rev = []
for rs in r_share:
    d = rs['d']
    delta = daily_delta[d]
    daily_rev.append({
        'd': d,
        'total': delta,
        's': delta * rs['s'] / 100,
        'm': delta * rs['m'] / 100,
        'l': delta * rs['l'] / 100,
        'x': delta * rs['x'] / 100,
    })

# 分区间汇总（日均绝对贡献）
stages = [(0,6,'D0-D6',7),(7,13,'D7-D13',7),(14,17,'D14-D17',4),(18,20,'D18-D20',3),(14,20,'D14-D20',7),(21,30,'D21-D30',10)]

print('='*95)
