print(f'{"区间":<10} {"日均总LTV":>10} {"小R日均":>10} {"中R日均":>10} {"大R日均":>10} {"超R日均":>10}')

best = {'s':('',0),'m':('',0),'l':('',0),'x':('',0),'total':('',0)}
stage_data = []
for s_start, s_end, label, days in stages:
    sums = {'s':0,'m':0,'l':0,'x':0,'total':0}
    for dr in daily_rev:
        if s_start <= dr['d'] <= s_end:
            for k in sums: sums[k] += dr[k]
    avgs = {k: sums[k]/days for k in sums}
    stage_data.append((label, avgs))
    for k in best:
        if avgs[k] > best[k][1]: best[k] = (label, avgs[k])
    print(f'{label:<10} {avgs["total"]*1000:>10.2f}‰ {avgs["s"]*1000:>10.2f}‰ {avgs["m"]*1000:>10.2f}‰ {avgs["l"]*1000:>10.2f}‰ {avgs["x"]*1000:>10.2f}‰')

print(f'\n日均贡献峰值区间:')
