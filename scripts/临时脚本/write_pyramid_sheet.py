def gws_update(range_str, values):
    """Write values to sheet via gws stdin"""
    cmd_input = json.dumps({
        "args": [
            "sheets", "spreadsheets", "values", "update",
            "--params", json.dumps({
                "spreadsheetId": SSID,
                "range": f"'{SHEET}'!{range_str}",
                "valueInputOption": "USER_ENTERED"
            }),
            "--json", json.dumps({
                "values": values
            })
        ]
    }, ensure_ascii=False)

    result = subprocess.run(
        ["node", "C:/ADHD_agent/scripts/gws_stdin.js"],
        input=cmd_input, capture_output=True, text=True, encoding='utf-8'
    )
    if result.returncode != 0:
        print(f"  ERROR writing {range_str}: {result.stderr[:200]}")
        return False
    else:
        resp = json.loads(result.stdout) if result.stdout else {}
        cells = resp.get("updatedCells", "?")
        print(f"  OK: {range_str} ({cells} cells)")
        return True

