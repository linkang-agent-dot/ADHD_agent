config_rows = []
for line in activity_config_data.strip().split('\n')[1:]:
    parts = line.split(',', 1)
    config_rows.append({'id': int(parts[0]), 'comment': parts[1]})

# 解析映射表
mapping_activity_ids = set()
mapping_rows = []
for line in lifecycle_mapping_raw.strip().split('\n')[1:]:
    parts = line.split('\t')
    if len(parts) >= 2 and parts[1].strip().isdigit():
        act_id = int(parts[1].strip())
        mapping_activity_ids.add(act_id)
        mapping_rows.append({
            'lifecycle_id': parts[0].strip(),
            'activity_id': act_id,
            'comment': parts[2].strip() if len(parts) > 2 else ''
        })

