def write_sheet(sheet_name, values):
    params = json.dumps({
        'spreadsheetId': SPREADSHEET_ID,
        'range': f'{sheet_name}!A1',
        'valueInputOption': 'USER_ENTERED'
    }, ensure_ascii=False)
    body = json.dumps({'values': values}, ensure_ascii=False)
    result = subprocess.run(
        [GWS_CMD, 'sheets', 'spreadsheets', 'values', 'update',
         '--params', params, '--json', body],
        capture_output=True
    )
    out = result.stdout.decode('utf-8', errors='replace')
    try:
        data = json.loads(out)
        rows = data.get('updatedRows', data.get('updatedCells', '?'))
        print(f"  OK: {sheet_name} -> {rows} rows/cells")
        return True
    except:
        print(f"  FAIL: {sheet_name} -> {out[:100]}")
        return False

